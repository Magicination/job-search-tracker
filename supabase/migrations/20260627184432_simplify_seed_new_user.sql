-- Обновление seed_new_user(): экраны "Сегодня", "Недельный график" и
-- "Прогресс" (с английским/SQL) убраны из интерфейса — больше нет смысла
-- заполнять schedule_slots/study_hours/tasks при регистрации нового
-- пользователя, это просто мёртвые данные, которые никто не увидит.
--
-- Это `create or replace function` — заменяет ТЕЛО уже существующей
-- функции, не трогая структуру таблиц или ранее накопленные данные у
-- существующих пользователей (их старые schedule_slots/study_hours/tasks
-- остаются как есть, просто новые пользователи их больше не получают).

create or replace function public.seed_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  -- Сидирование пока не нужно ни для одной из оставшихся сущностей —
  -- отклики, версии резюме/сопроводительных и расшаренные ресурсы
  -- создаются пользователем самостоятельно, без стартового набора данных.
  return new;
end;
$$;
